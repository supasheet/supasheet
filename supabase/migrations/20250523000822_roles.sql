-- Performance indexes
create index idx_user_roles_user_id on supasheet.user_roles (user_id);

create index idx_user_roles_role on supasheet.user_roles (role);

create index idx_role_permissions_role on supasheet.role_permissions (role);

create index idx_role_permissions_permission on supasheet.role_permissions (permission);

create index idx_role_permissions_role_permission on supasheet.role_permissions (role, permission);

create or replace function supasheet.has_permission (requested_permission supasheet.app_permission) returns boolean as $$
declare
  _user_id uuid;
begin
  _user_id := (select auth.uid()); -- evaluated once
  return exists (
    select 1
    from supasheet.role_permissions rp
    inner join supasheet.user_roles ur on rp.role = ur.role
    where ur.user_id = _user_id
      and rp.permission = requested_permission
  );
end;
$$ language plpgsql stable security definer
set
  search_path = '';

grant
execute on function supasheet.has_permission (supasheet.app_permission) to authenticated,
service_role;

create or replace function supasheet.has_role (requested_role supasheet.app_role) returns boolean as $$
declare
  _user_id uuid;
begin
  _user_id := (select auth.uid());
  return exists (
    select 1 from supasheet.user_roles
    where user_id = _user_id and role = requested_role
  );
end;
$$ language plpgsql stable security definer
set
  search_path = '';

create index on supasheet.user_roles using btree (user_id);

grant
execute on function supasheet.has_role (supasheet.app_role) to authenticated,
service_role;

create policy "User can view their own role" on supasheet.user_roles for
