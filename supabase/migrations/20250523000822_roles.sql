begin;

create type supasheet.app_permission as enum(
  'supasheet.users:select',
  'supasheet.users:select_all',
  'supasheet.users:update',
  'supasheet.users:insert',
  'supasheet.users:delete',
  'supasheet.users:invite',
  'supasheet.users:ban',
  'supasheet.users:generate_link',
  'supasheet.user_roles:select',
  'supasheet.user_roles:select_all',
  'supasheet.user_roles:insert',
  'supasheet.user_roles:delete',
  'supasheet.role_permissions:select',
  'supasheet.role_permissions:select_all',
  'supasheet.role_permissions:insert',
  'supasheet.role_permissions:delete'
);

create type supasheet.app_role as enum('x-admin', 'admin', 'user');

commit;

-- USER ROLES
create table supasheet.user_roles (
  id bigint generated by default as identity primary key,
  user_id uuid references supasheet.users on delete cascade not null,
  role supasheet.app_role not null,
  unique (user_id, role)
);

comment on table supasheet.user_roles is '{
  "label": "User Roles",
  "icon": "UserCheckIcon"
}';

alter table supasheet.user_roles enable row level security;

revoke all on table supasheet.user_roles
from
  authenticated,
  service_role;

grant
select
,
  insert,
update,
delete on table supasheet.user_roles to authenticated,
service_role;

-- ROLE PERMISSIONS
create table supasheet.role_permissions (
